#include <iostream> 
using namespace std ;
 int main (){

    // types of Bitwise operator
    // 1, & (and)
    // 2, ! (or)
    // 3, ^ (Xor)
    // 4, ~ (Negation)
    // 5, >> (right shift)
    // 6, << (left shift)

    int k;
    k= 8 & 23;
    cout << k << "\n";


    k = 8 && 23 ;
    cout << k ;
 }